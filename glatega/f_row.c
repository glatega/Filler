#include "filler.h"

int			f_row(t_grid token)
{
	int		x;
	int		y;

	y = 0;
	while (y < token.h)
	{
		x = 0;
		while (x < token.w)
		{
			if (token.data[y][x] == '*')
				return (y);
			x++;
		}
		y++;
	}
	return (y);
}
