#include "filler.h"

int			f_col(t_grid token)
{
	int		x;
	int		y;

	x = 0;
	while (x < token.w)
	{
		y = 0;
		while (y < token.h)
		{
			if (token.data[y][x] == '*')
				return (x);
			y++;
		}
		x++;
	}
	return (x);
}
