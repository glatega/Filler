#include "filler.h"

int			l_row(t_grid token)
{
	int		x;
	int		y;

	y = token.h - 1;
	while (y >= 0)
	{
		x = 0;
		while (x < token.w)
		{
			if (token.data[y][x] == '*')
				return (y);
			x++;
		}
		y--;
	}
	return (y);
}
