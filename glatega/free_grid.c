#include "filler.h"

void	free_grid(t_grid *grid)
{
	int		i;

	i = 0;
	while (i < grid->h)
	{
		ft_memdel((void**)&((grid->data)[i]));
		i++;
	}
	ft_memdel((void**)&(grid->data));
}
