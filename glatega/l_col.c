#include "filler.h"

int			l_col(t_grid token)
{
	int		x;
	int		y;

	x = token.w - 1;
	while (x >= 0)
	{
		y = 0;
		while (y < token.h)
		{
			if (token.data[y][x] == '*')
				return (x);
			y++;
		}
		x--;
	}
	return (x);
}
